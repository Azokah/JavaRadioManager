package dbclases;

public class Productor {

	private String nombre;
	private float sueldo;
	public Productor(String nombre, float sueldo) {
		this.nombre = (nombre);
		this.sueldo = (sueldo);
	}
	public String getNombre() {
		return nombre;
	}
	public float getSueldo() {
		return sueldo;
	}

}
